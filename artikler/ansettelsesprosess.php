        <p class="paragraph-9"><strong><em>Under har vi samlet noen nøkkelpunkter som kan være til hjelp både for rekrutterer og<br>kandidaten i en ansettelsesprosess:</em></strong><br></p>
        <ol role="list" class="list-2">
          <li class="list-item-2">Kandidater som har undersøkt bedriften og satt seg inn i stillingen som er utlyst på<br>forhånd, viser iver for stillingen og selskapet.</li>
          <li class="list-item-2">Kandidater som har undersøkt bedriften og satt seg inn i stillingen som er utlyst på<br>forhånd, viser iver for stillingen og selskapet.</li>
          <li class="list-item-2">Ærlighet; en kandidat som kjenner seg selv, både på en positiv og mindre positiv måte sier mye om kandidaten.</li>
          <li class="list-item-2">En god flyt i samtalen gir et inntrykk og et godt grunnlag for å se om kandidaten er<br>den bedriften ser etter.</li>
          <li class="list-item-2">Grundig bakgrunnssjekk av kandidaten kan gi en indikasjon på inntrykket som<br>kandidaten gir av seg selv i rekrutteringsprosessen.</li>
          <li>Kandidatens kompetanse, tidligere erfaringer og arbeid kan gi en indikasjon på om<br>ovennevnte punkter stemmer med inntrykket kandidaten har gitt og er liten pekepinn<br>både for rekrutterer og kandidaten på hvordan kandidaten vil passe inn i bedriften.</li>
        </ol>
        <div class="line-90"></div>
        <p class="paragraph-9">Innkalling til intervju betyr at kandidaten i utgangspunktet vil passe og er kvalifisert til<br>stillingen og bedriften. Rekrutteringsprosessen handler om å undersøke om kandidaten<br>besitter de personlige egenskapene og er den bedriften er på jakt etter. Selve<br>rekrutteringsprosessen, kandidatens væremåte og svar under intervjuet er helt avgjørende<br>for om kandidaten ender med å få jobbtilbudet i bedriften.<br></p>
