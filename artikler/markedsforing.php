      <div class="paragraph-container">
        <p class="paragraph-10">Markedsføring kan enkelt defineres som salgsfremmende tiltak med formål om å promotere eller selge produkter og tjenester, ved annonsering og analysering av markedet.<br></p>
        <p class="paragraph-9">Markedsføring av virksomheter er avhengig av et overordnet mål som markedsstrategien kan innrettes etter. For å kunne formulere en slik markedsstrategi, må en sette seg inn i grunnleggende konsepter for markedsføring og salg. Markedsføring dreier seg om mer enn bare kommunikasjon og potensielle kunder. Hvor synlig din bedrift er, påvirkes av målet og måten din bedrift er bygget opp. Her kommer de 4 P-ene inn, Produkt, Pris, Plass (distribusjon) og Promotering. De 4 P-ene er de viktigste brikkene i en hver strategi for<br>markedsføring. Uten disse er det vanskelig å danne nyanserte strategier for bedriften som rettes mot spesifikke kunder.<br></p>
        <div class="image-section-2"><img src="../images/danielle-macinnes-IuLgi9PWETU-unsplash.jpg" loading="lazy" sizes="(max-width: 479px) 90vw, (max-width: 1919px) 70vw, 100vw" srcset="../images/danielle-macinnes-IuLgi9PWETU-unsplash-p-1080.jpeg 1080w, ../images/danielle-macinnes-IuLgi9PWETU-unsplash-p-1600.jpeg 1600w, ../images/danielle-macinnes-IuLgi9PWETU-unsplash.jpg 1920w" alt="" class="image-16">
          <div class="photo-text">FOTO: DANIELLE MCINESS</div>
        </div>
        <p class="paragraph-9">For å etablere en solid merkevare og en klar visjon fra første dag er det viktig å gjennomgå de 4 P-ene. Dette vil legge et solid grunnlag for videre drift, og styrker hvert valg og kampanje som dannes deretter.<br><br><strong><em>Den grunnleggende modellen for markedsføring, de 4 P-ene, deles inn i fire kategorier:</em></strong><br></p>
        <ol role="list">
          <li class="list-item-2"><strong>Produkt:</strong> En gjenstand eller tjeneste som søker å fylle et behov i et marked.<br>Hva selges? Hvor høy er etterspørselen? Konkurransenivået?</li>
          <li class="list-item-2"><strong>Pris:</strong> kostnaden en forbruker betaler for et produkt.<br>Kostnader? Markedspris? Tilbud og rabatter? Fortjenestegrunnlag?</li>
          <li class="list-item-2"><strong>Plass:</strong> Distribusjon, den prosessen der et produkt eller en tjeneste gjøres tilgjengelig for en forbruker.<br>Tilgjengelighet? Distribusjon? Kundekontakt?</li>
          <li class="list-item-6"><strong>Promotering:</strong> annonsering, utnytting av kontaktnettverk og markedsføringsstrategi.<br>Markedsføringsstrategi? Annonsering? Kontaktnettverk?</li>
        </ol>
        <div class="line-90"></div>
        <p class="paragraph-9">Markedsføring dreier seg om mer enn å selge varer, tjenester og ideer. Det dreier seg og om å investere i fornøyde kunder. Kundeorientering eller markedsorientering forutsetter oppdatert kunnskap om kundens behov, livsstil, holdninger, væremåte, intensjoner, ønsker både før, etter og under et kjøp og bruk av en vare eller tjeneste.<br></p>
