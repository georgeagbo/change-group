      <div class="paragraph-container">
        <p class="paragraph-10">Media er en rekke kommunikasjonskanaler som kan formidle et budskap til mange mennesker på kort tid. Media er en fellesbetegnelse for alle meddelelsesmidler som kan kommunisere til en eller flere mottakere.<br><br>Meddelelsesmidler som video, er et medie som stadig flere bruker som en del av markedsføringen, og det med god grunn. Video er blitt populært og vokser raskt. Video er blitt en del av bedriftenes mediemiks og strategi.<br></p>
        <p class="paragraph-9">Forbrukere flest foretrekker video fremfor tekst eller bilder. Videoer engasjerer og påvirker oss emosjonelt. Den digitale verden er en stor del av den den generelle forbrukerens hverdag. Hvordan, når og hva forbrukeren vil se, er det forbrukeren selv som bestemmer. Der er uendelig av innhold og videoer på nett, men en video som engasjerer og skaper reaksjoner vil være den videoen som kommer gjennom med sitt budskap.<br></p>
        <div class="image-section-2"><img src="../images/sirisvisual-hGkYk7bMrLg-unsplash.jpg" loading="lazy" sizes="(max-width: 479px) 90vw, (max-width: 1919px) 70vw, 100vw" srcset="../images/sirisvisual-hGkYk7bMrLg-unsplash-p-1080.jpeg 1080w, ../images/sirisvisual-hGkYk7bMrLg-unsplash-p-1600.jpeg 1600w, ../images/sirisvisual-hGkYk7bMrLg-unsplash.jpg 1920w" alt="" class="image-16">
          <div class="photo-text">FOTO: SIRIUSVISUAL</div>
        </div>
        <p class="paragraph-9">Video kan være med på å bidra til å øke bedriftens rangering. Grunnen til dette er blant<br>annet at sosiale medier og søkemotorer foretrekker video og prioriterer dette i sine<br>algoritmer. Bedriften har dermed større sjanse for å havne på førstesiden på Google eller<br>andre søkemotorer, om bedriften bruker videoer i sine nettsider.<br><br>Noe som kan øke konverteringsraten på nettsiden er en informativ og godt utviklet<br>produktvideo. Video er en lønnsom investering. Video gir også en større tillit til nettkjøp, og<br>trigger til direkte salg. Det har seg ofte sånn at kjøper som ser en video ofte kommer tilbake<br>for å kjøpe produktet kunden har sett. Dette gir både måloppnåelse på kort tid og bedre<br>avkastning over lengre tid.<br><br>Dagens samfunn bruker mobilen sin til aktivitet på internett mer enn noen gang. Det å<br>tilrettelegge markedsføringsaktivitetene slik at de er kompatible på mobil er derfor viktig. Like<br>viktig er det å tilpasse forbrukeren som bruker mobilen. Som oftest foregår mye av<br>aktiviteten på farten eller det skal sjekkes noe raskt. I dagens samfunn skal aktiviteten skje<br>så fort som mulig, derfor er video å foretrekke fremfor en lang tekst.<br></p>
        <div class="line-90"></div>
        <p class="heading-2-big">Fordelen med video er at det er et allsidig medium. Muligheten ved å lage videoer er<br>uendelige. Videoen kan lages til alle stadier i kundens kjøpsreise og til alle mulig formål. Strategisk målrettede videoer kan gjøre at kunden tilegner seg kunnskap og informasjon om<br>dine produkter og tjenester som igjen ender med at kunden kjøper akkurat dine produkter<br>eller tjenester.</p>
        <p class="paragraph-9">Reklame er bygget opp på den måten at det ikke lenger skal oppfattes som reklame. Video laget på en interessante måte, kan være med på å bygge tillit. Spennende budskap, interessante vinklinger eller at kunden kjenner seg igjen, gjort på en riktig måte gjør at kunden føler å ha fått noe igjen etter å ha sett videoen. Dette skaper gode relasjoner mellom forbruker og bedrift.<br><br>Det er lite som er mer delbart mellom venner og bekjente enn en god video. Video kan lett deles fra en interessert forbruker videre til dens bekjentskapskrets. I dag lever samfunnet for sosiale medier både på godt og vondt, og avgjørende faktorer for deling av en video, er som regel følelser som glede, sinne eller melankoli. En video som inneholder disse faktorene har størst delingsverdi.<br><br>Video er et meddelelsesmiddel som kommunisere alt fra tekster, produktinformasjon, bedriftens genuine stolthet eller kundecaser. Video kan å være med på å gjøre at din bedrift skiller seg ut fra konkurrentene. Ved å fremme bedriftens produkter, vise bruksmåte eller kvaliteter på en kreativ måte er avgjørende i et marked styrt av lavfrekvente kjøpere, og liten grad av direkte involvering i kjøpsbeslutningene. Hvis et bilde sier mer enn 1000 ord, må video si minst en million?<br></p>
