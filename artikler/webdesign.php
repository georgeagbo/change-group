        <p class="paragraph-9">Webdesignerene tar høyde for oppbyggingen når de designer en nettside. Ved å legge den viktigste informasjonen øverst til venstre på siden, kommer budskapet lettere frem til potensielle kunder. Ved å plassere informasjonen i disse områdene, forbedres også nettsidens søkemotoroptimalisering (SEO). Når stikkord som er relatert til bedriften inkluderes i denne teksten, blir det lettere for Google og de andre søkemotorene å forstå innholdet på siden. Dette forbedrer nettsidens plassering på søkemotorene, og gjør at siden blir lettere å oppdage. Innholdet på nettsiden bør derfor bestå av korte og direkte meldinger. Nettsidens innhold og grafisk design er selvfølgelig viktig for å presentere bedriften for den besøkende og teksten bør formuleres slik at leseren føler seg velkommen til siden. Med et godt skrevet innhold blir nettsiden behagelig og lesbar for den besøkende.<br></p>
