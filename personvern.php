            <div>Vi kan oppdatere / endre denne policyen. Ved endring, vil vi varsle deg på vår hjemmeside www.changegroup.no. Vi vil også avsløre hvilke endringer vi gjorde i politikken på tidspunktet for endringen. Du kan alltid kontakte oss hvis du vil ha tilgang til tidligere versjoner.</div>
