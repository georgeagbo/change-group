            <h2 class="heading-18"><strong class="bold-text-5">Hvordan håndterer vi dine personlige opplysninger</strong></h2>
            <div class="text-block-7">For å kunne tilby deg våre tjenester / produkter, må vi behandle visse personlige data / data om deg eller ditt besøk på vår nettside. Vi beskytter ditt personvern og sikkerhet naturligvis og samler derfor ikke mer informasjon enn vi trenger. Informasjonen blir selvfølgelig aldri solgt til tredjeparter. Har du spørsmål eller bekymringer, vennligst kontakt oss på: kontakt@changegroup.no<br></div>
            <h2 class="heading-18"><strong>Innsamling og bruk av data</strong></h2>
            <h2 class="heading-small"><strong class="bold-text-3">Hvilken informasjon samler vi inn?</strong></h2>
            <div>Denne databeskyttelsespolitikken dekker alle data vi samler inn, for eksempel fra nettsider, sosiale medier, kontakt via telefon eller post og på arrangementer / messer. Vi kan kombinere personopplysninger som samles på en måte (for eksempel et nettsted) med personopplysninger samlet på en annen måte.</div>
            <h2 class="heading-18"><strong>Informasjon du gir oss</strong></h2>
            <div>Du kan direkte eller indirekte gi oss informasjon om deg eller din bedrift på flere måter, for eksempel når du bestiller våre tjenester / produkter eller kontakt oss via skjemaer / mail fra vår nettside.</div>
            <h2 class="heading-18"><strong>Denne informasjonen kan omfatte:</strong></h2>
            <ul role="list">
              <li class="list-item-2">
                <div>Person- og kontaktinformasjon – Navn, Adresse, E-postadresse, Mobiltelefonnummer</div>
              </li>
              <li class="list-item-2">
                <div class="text-block-6">Informasjon om varer / tjenester – detaljer om varene / tjenestene du har kjøpt.</div>
              </li>
              <li class="list-item-2">
                <div>Geografisk informasjon – Din geografiske plassering.</div>
              </li>
            </ul>
            <div>Informasjonen vi samler er nødvendig for å inngå en avtale med oss, for å handle online eller for å sitere et tilbud. Personlige tall behandles kun i unntakstilfeller for visse tjenester som krever legitim identitet. Les også mer om hvordan vi bruker informasjonskapsler i vår informasjonskapsel.</div>
            <h2 class="heading-18"><strong class="bold-text-6">Hvorfor samler vi inn informasjon og hva gjør vi med informasjonen?</strong></h2>
            <h2 class="heading-small"><strong>Din informasjon forblir hos oss!</strong></h2>
            <div>Vi vil aldri selge eller dele informasjonen din til tredjepart uten din tillatelse. Noen ganger deler vi personlig informasjon med leverandører eller underleverandører for å fullføre / levere vår forpliktelse til deg. Alle våre partnere er selvsagt underlagt personvern og, selvfølgelig, oppfyller juridiske og tekniske krav for at dine opplysninger skal håndteres trygt.</div>
            <h2 class="heading-18"><strong>Hvor behandler vi dine personlige opplysninger?</strong></h2>
            <div>Vi forsøker alltid å lagre dine personopplysninger i Sverige / EU / EØS. Noen sjeldne tilfeller kan kreve at visse oppgaver blir håndtert av en tredjepart (som teknologileverandør / underleverandør) Vi vil imidlertid alltid ta skritt til data som sendes / lagret på denne måten blir håndtert på samme nivå av sikkerhet innen EU / EØS.</div>
            <h2 class="heading-18"><strong>Hvor lenge lagrer vi dine personlige opplysninger?</strong></h2>
            <div>Vi lagrer data så lenge det er nødvendig for å oppfylle formålet som informasjonen ble samlet inn, eller for å utføre våre forpliktelser og så lenge det er nødvendig av lovbestemte oppbevaringsperioder, særlig med hensyn til krav til rapportering.</div>
            <h2 class="heading-18"><strong>Dine rettigheter</strong></h2>
            <ul role="list">
              <li class="list-item-2">
                <div>Du som er kunde / registrert hos oss av en eller annen grunn har flere rettigheter du burde vite.</div>
              </li>
              <li>
                <div class="text-block-6">Du er fri til å be om et registreringsdokument for hvilken informasjon om deg vi har lagret om deg / din bedrift.</div>
              </li>
              <li class="list-item-2">
                <div>Du har rett til å få informasjonen rettet hvis de er feil, ufullstendig eller villedende på noen måte. Du kan også begrense behandlingen av personopplysninger til oppdatering.</div>
              </li>
              <li class="list-item-2">
                <div>Du har rett til å bli glemt. Sletting av personopplysninger skjer imidlertid ikke hvis nødvendig for å fullføre vår forpliktelse / avtale med deg, eller om en annen svensk lov, myndighet eller domstol sier noe annet. Skulle du synes det ikke er noen legitime grunner til dette, har du selvsagt rett til å motsette seg behandlingen av informasjonen din.</div>
              </li>
              <li class="list-item-2">
                <div>Rett til å sende inn klage, trekke tilbake samtykke.</div>
